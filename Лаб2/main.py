import csv    # Підключаємо модуль для роботи з файлами формату CSV
import numpy as np        # Підключаємо бібліотеку для зручної і швидкої роботи з масивами чисел
import matplotlib.pyplot as plt   # Підключаємо бібліотеку, яка буде малювати нам красиві графіки

def read_data(filename): # Створюємо функцію, яка навчить програму читати наш файл
    x, y = [], []               # Створюємо два порожні кошики (списки): для розмірів даних (x) та часу (y)
    with open(filename, 'r', newline='') as file: # Відкриваємо файл 'data.csv' виключно для читання
        reader = csv.DictReader(file)  # Кажемо програмі читати цей файл як таблицю, де є заголовки колонок
        for row in reader:      # Запускаємо цикл, який пройдеться по кожному рядочку нашої таблиці
            x.append(float(row['n']))     # Беремо число з колонки 'n', робимо його дробовим і кидаємо в кошик x
            y.append(float(row['t']))     # Беремо число з колонки 't', робимо його дробовим і кидаємо в кошик y
    return np.array(x), np.array(y)   # Перетворюємо наші звичайні списки у супермасиви numpy і віддаємо їх назад

def divided_diff(x, y):      # Створюємо функцію для побудови таблиці розділених різниць
    n = len(y)           # Дізнаємося, скільки всього у нас є точок (у нашому випадку їх 5)
    table = np.zeros((n, n))
    table[:, 0] = y             # Записуємо в найперший стовпець цієї таблиці всі наші відомі значення часу
    for j in range(1, n):         # Починаємо крокувати по стовпцях зліва направо, щоб заповнити всю таблицю
        for i in range(n - j):     # Крокуємо по рядках зверху вниз (щоразу на один рядок менше, роблячи "трикутник")
            table[i][j] = (table[i + 1][j - 1] - table[i][j - 1]) / (x[i + j] - x[i]) # Рахуємо різницю за формулою з методички
    return table[0, :]    # Для формули Ньютона нам потрібен тільки найперший (верхній) рядок таблиці

def newton_interpolate(x_data, y_data, x_val): # Створюємо функцію самого многочлена Ньютона для нашого прогнозу
    coef = divided_diff(x_data, y_data)   # Просимо попередню функцію порахувати і віддати нам готові коефіцієнти
    n = len(x_data)             # Знову рахуємо, скільки всього у нас вузлів (щоб знати, скільки разів додавати)
    result = coef[0]          # Перший шматочок нашого рівняння — це просто перше значення з таблиці (f(x0))
    for k in range(1, n):     # Запускаємо цикл, щоб додати до результату всі інші складні доданки
        w = 1      # Створюємо змінну w, яка буде накопичувати множення дужок виду (x - xi)
        for i in range(k):     # Проходимося по попередніх точках, щоб зібрати правильну кількість дужок
            w *= (x_val - x_data[i])    # Домножуємо нашу змінну на чергову дужку з відповідним вузлом
        result += w * coef[k]      # Множимо зібрані дужки на коефіцієнт з таблиці і додаємо до загального результату
    return result              # Повертаємо готове спрогнозоване число (час виконання для нової точки)

filename = "data.csv"      # Записуємо в змінну точну назву нашого файлу з початковими даними

x_data, y_data = read_data(filename)   # Викликаємо функцію читання і записуємо отримані дані у дві змінні

target_n = 6000    # Задаємо точку, для якої хочемо дізнатися прогноз (згідно з вашим Варіантом 1)
predicted_t = newton_interpolate(x_data, y_data, target_n) # Запускаємо математику Ньютона, щоб знайти час для n=6000

print(f"Прогноз: час виконання алгоритму для n={target_n} становить {predicted_t:.2f} мс") # Виводимо знайдений результат на екран

x_smooth = np.linspace(min(x_data), max(x_data), 100)
y_smooth = [newton_interpolate(x_data, y_data, xi) for xi in x_smooth] # Рахуємо висоту кожної з цих 100 точок за допомогою Ньютона

plt.figure(figsize=(10, 6))       # Створюємо порожнє полотно для малювання графіка розміром 10 на 6 дюймів

plt.plot(x_smooth, y_smooth, color='blue', label='Многочлен Ньютона (Інтерполяція)') # Малюємо знайдену криву плавною синьою лінією
plt.scatter(x_data, y_data, color='red', zorder=5, label='Експериментальні дані') # Ставимо жирні червоні крапки на місці відомих даних
plt.scatter(target_n, predicted_t, color='green', marker='*', s=150, zorder=6, label=f'Прогноз (n={target_n})') # Малюємо зелену зірку-прогноз

plt.title("Прогнозування часу виконання алгоритму") # Пишемо великий красивий заголовок над нашим графіком
plt.xlabel("Розмір вхідних даних (n)")    # Підписуємо, що означає нижня горизонтальна вісь (X)
plt.ylabel("Час виконання (t, мс)")    # Підписуємо, що означає ліва вертикальна вісь (Y)
plt.grid(True, linestyle='--')       # Вмикаємо на фоні пунктирну сітку, щоб було легше дивитися координати
plt.legend()                 # Виводимо в куточку віконце-підказку, яке пояснює всі кольори на графіку
plt.show()               # Даємо фінальну команду: "Відкрий вікно і покажи нам готовий малюнок!"