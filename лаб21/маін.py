import numpy as np  # Підключаємо бібліотеку для швидких математичних обчислень
import matplotlib.pyplot as plt  # Підключаємо бібліотеку для малювання графіків


def divided_diff(x, y):  # Функція для побудови таблиці розділених різниць (як у першому коді)
    n = len(y)       # Дізнаємося, скільки точок передали у функцію
    table = np.zeros((n, n))
    table[:, 0] = y        # У перший стовпець записуємо відомі значення Y
    for j in range(1, n):  # Запускаємо цикл по стовпцях таблиці
        for i in range(n - j):      # Запускаємо цикл по рядках, звужуючи їх донизу (як трикутник)
            table[i][j] = (table[i + 1][j - 1] - table[i][j - 1]) / (x[i + j] - x[i])  # Обчислюємо різниці за формулою
    return table[0, :]          # Віддаємо тільки найперший (верхній) рядок таблиці


def newton_interpolate(x_data, y_data, x_val):             # Функція самого многочлена Ньютона
    coef = divided_diff(x_data, y_data)  # Беремо коефіцієнти з попередньої функції
    result = coef[0]        # Стартуємо з першого доданка (значення f(x0))
    for k in range(1, len(x_data)):  # Цикл для додавання всіх наступних складних частин формули
        w = 1       # Змінна для накопичення дужок
        for i in range(k):      # Цикл, що збирає дужки (x - xi) докупи
            w *= (x_val - x_data[i])     # Домножуємо на чергову дужку
        result += w * coef[k]          # Множимо дужки на коефіцієнт і додаємо до загальної суми
    return result           # Повертаємо пораховане значення для нової точки


def true_function(x):  # Створюємо штучну функцію для перевірки алгоритму на міцність
    t = (2 * x - 17000) / 15000  # Масштабуємо нашу вісь X, щоб вона помістилася в класичний "дзвіночок" Рунге
    return 85 / (1 + 25 * t ** 2)  # Повертаємо значення класичної складної функції Рунге


x_smooth = np.linspace(1000, 16000, 500)
y_true = true_function(x_smooth)  # Обчислюємо ідеальні (еталонні) значення Y для цих 500 точок

fig, axes = plt.subplots(1, 3, figsize=(16, 5))    # Створюємо велике полотно розміром 16х5 і ділимо його на 3 рівні віконця (графіки)
fig.suptitle("Дослідження Ефекту Рунге", fontsize=16, fontweight='bold')     # Даємо жирний головний заголовок усьому вікну

nodes_list = [5, 10, 20]       # Створюємо список, де вказано, скільки вузлів брати для кожного з 3-х графіків

for i, n_nodes in enumerate(nodes_list):    #  Запускаємо цикл: проходимося по наших трьох експериментах (5, 10 та 20 точок)
    x_nodes = np.linspace(1000, 16000, n_nodes)
    y_nodes = true_function(x_nodes)     # Знаходимо еталонну висоту для кожного з цих створених вузлів

    y_pred = [newton_interpolate(x_nodes, y_nodes, xi) for xi in x_smooth]  # Просимо "Ньютона" побудувати криву суворо по цих червоних вузлах

    ax = axes[i]  # Вибираємо поточне віконце для малювання (перше, друге або третє)
    ax.plot(x_smooth, y_true, 'g--', linewidth=2,
            label='Ідеальна функція')      # Малюємо еталонну функцію зеленим пунктиром
    ax.plot(x_smooth, y_pred, 'b-', linewidth=2,
            label=f'Многочлен Ньютона')     # Малюємо спробу Ньютона суцільною синьою лінією
    ax.scatter(x_nodes, y_nodes, color='red', zorder=5,
               label=f'Вузли (n={n_nodes})')    # Ставимо жирні червоні крапки там, де ми брали вузли

    ax.set_ylim(-20, 150)  # Жорстко обмежуємо рамки малюнка, щоб лінія не стискала його, коли полетить у нескінченність
    ax.set_title(f"Кількість вузлів: {n_nodes}")     # Підписуємо кожне окреме віконечко
    ax.set_xlabel("Розмір даних (n)")     # Підписуємо горизонтальну вісь
    ax.set_ylabel("Час (t, мс)")       # Підписуємо вертикальну вісь
    ax.grid(True, linestyle='--')        # Вмикаємо сітку для зручності
    ax.legend()           # Додаємо підказку про кольори в куток графіка

plt.tight_layout()          # Автоматично розсовуємо графіки, щоб вони красиво виглядали і не налізали одне на одного
plt.show()             # Виводимо фінальний малюнок на екран!